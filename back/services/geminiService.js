const { OpenAI } = require("openai");
require("dotenv").config();

const openai = new OpenAI({
  baseURL: "https://api.deepseek.com/v1",
  apiKey: process.env.DEEPSEEK_API_KEY,
});

async function DeepSeekCompare(compareData) {
  const listingsText = compareData.compareList
    .map(
      (item) =>
        `ID: ${item.id}, Title: ${item.title}, Price: ${item.price}€, Condition: ${item.condition_category}, Seller Rating: ${item.seller_rating || "N/A"}, Reviews: ${item.seller_reviews_count || "N/A"}, Description: ${item.clean_description || "No description"}`,
    )
    .join("\n");

  const historyText = compareData.chatHistoryText && compareData.chatHistoryText.length > 0
    ? compareData.chatHistoryText.map(msg => `[${msg.role}]: ${msg.content}`).join("\n")
    : "История пуста";

  const systemPrompt = `Ты — прагматичный рыночный аналитик, инвестиционный консультант и независимый риск-менеджер. Твоя цель — защитить капитал клиента от невыгодных вложений, скрытых издержек и переплат.

Ты получаешь структурированные технические описания лотов. Твоя задача — сопоставить технические ограничения (описанные экспертом-аудитором) с рыночной стоимостью, ликвидностью и рентабельностью предложения.

<ЛОГИКА_АНАЛИЗА_РЫНКА>
1. Экономика рисков: Если технический аудит указывает на "прогноз стабильной работы: 0 месяцев" или наличие врожденных конструктивных нюансов (высокий нагрев процессора, низкий класс энергоэффективности здания DPE, большой пробег авто), ты должен оценить, компенсирует ли текущая цена лота эти недостатки. Если цена низкая — лот может быть рентабельным (Грейд A/B). Если цена высокая при больших рисках — это переплата (Грейд C).
2. Критерий ликвидности: Оценивай платформу/архитектуру товара. Устаревающие, тупиковые или требующие скорых вложений для легальной эксплуатации позиции не могут претендовать на высший грейд (S), если только их цена не является экстремально заниженной.
3. Внереестровые рекомендации: Если весь список товаров признан тобой неэффективным (соотношение цена/риск неудовлетворительное), ты обязан прямо в поле "chat_reply" предложить альтернативные векторы поиска (другие поколения, смежные бренды, измененные критерии фильтрации).
</ЛОГИКА_АНАЛИЗА_РЫНКА>

<ПРАВИЛА_РЕЖИМОВ_И_ГРЕЙДОВ>
- По умолчанию всегда работай в режиме диалога (mode: chat). Категорически запрещено выводить тир-лист (рейтинг), пока в текущем запросе пользователя (frontMessage) не будет прямой и явной инструкции составить рейтинг, топ или тир-лист.
- Если данных достаточно для ранжирования, но явного запроса на тир-лист нет, проанализируй риски в "chat_reply", подсвети подводные камни и проактивно предложи сформировать тир-лист.
- При формировании тир-листа (mode: tier) выставляй грейды строго: S (идеально, цена ниже рынка, риски минимальны), A (хорошо, адекватная цена, контролируемые риски), B (компромисс, есть нюансы или цена на грани), C (плохо, высокий риск, сильная переплата). Без знаков +/-.
</ПРАВИЛА_РЕЖИМОВ_И_ГРЕЙДОВ>

Оформляй ответ СТРОГО в виде валидного JSON-объекта (без маркдауна и стороннего текста):
{
  "mode": "chat или tier",
  "chat_reply": "Естественный, глубокий аналитический ответ на русском языке с аргументацией рисков, оценкой целесообразности цены и рекомендациями альтернатив.",
  "results": [
    {
      "id": 0,
      "ai_verdict": "Предельно краткое экономическое обоснование итоговой оценки лота на основе баланса его цены, состояния и технических издержек.",
      "pros": ["аргументированный плюс 1", "аргументированный плюс 2"],
      "cons": ["аргументированный минус 1", "аргументированный минус 2"],
      "grade": "S/A/B/C"
    }
  ]
}
Если mode: "chat", массив results ОБЯЗАТЕЛЬНО должен быть пустым [].`;

  const userContent = `История диалога:\n${historyText}\n\nПозиции для финансово-рыночного анализа:\n${listingsText}\n\nТекущий запрос пользователя: "${compareData.frontMessage}"`;

  try {
    const response = await openai.chat.completions.create({
      model: "deepseek-chat",
      messages: [
        { role: "system", content: systemPrompt },
        { role: "user", content: userContent }
      ],
      response_format: { type: "json_object" },
      temperature: 0.3, // Оптимально для удержания жесткой структуры и аналитического мышления
      max_tokens: 8000
    });

    const rawText = response.choices[0].message.content;
    console.log('Raw DeepSeek response:', rawText);
    
    return JSON.parse(rawText);
  } catch (error) {
    console.error("[DeepSeek Error]: Ошибка при генерации тир-листа:", error);
    throw error;
  }
}

module.exports = { DeepSeekCompare };